// 根据风格和方向获取对应题库
export function getQuestionLayers(styleId, directionId) {
  if (styleId === 'a') {
    switch (directionId) {
      case 'legend': return legendQuestions;
      case 'brand':  return brandQuestions;
      case 'fury':   return furyQuestions;
      default:       return legendQuestions;
    }
  }
  if (styleId === 'b') return memoirQuestions;
  if (styleId === 'c') return funQuestions;
  return legendQuestions;
}

